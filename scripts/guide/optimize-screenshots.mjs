// Guide image optimizer — modern-format pipeline.
//
// For every real Moodle screenshot under public/guide/screenshots/ this emits
// next-gen siblings the browser can pick via <picture>:
//   foo.jpg  ->  foo.webp  +  foo.avif   (JPEG stays as the universal fallback)
// It also caps width at 1200px (idempotent) and writes a dimensions manifest
// (src/data/screenshotMeta.ts) so the UI can reserve exact space and never shift
// layout (CLS = 0). The Jerusalem logo gets a WebP sibling too.
//
// Run: npm run guide:optimize   (or: node scripts/guide/optimize-screenshots.mjs)
//
// Why per-format quality differs: AVIF holds sharp UI text at lower quality than
// WebP, so it can go a bit lower and still look crisp. Numbers tuned for
// screenshots (flat UI + text), not photos.

import { readdir, readFile, writeFile } from 'node:fs/promises';
import { join, extname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';
import sharp from 'sharp';

const ROOT = fileURLToPath(new URL('../..', import.meta.url));
const SHOTS_DIR = join(ROOT, 'public', 'guide', 'screenshots');
const LOGO = join(ROOT, 'public', 'guide', 'jerusalem-math-logo.png');
const MANIFEST = join(ROOT, 'src', 'data', 'screenshotMeta.ts');
const MAX_W = 1200;

const WEBP = { quality: 82, effort: 5 };
const AVIF = { quality: 56, effort: 4 };

function kb(n) {
  return `${Math.round(n / 1024)}KB`;
}

async function processImage(dir, file) {
  const src = join(dir, file);
  const ext = extname(file).toLowerCase();
  const stem = basename(file, ext);

  // Read the source into a buffer FIRST (avoids Windows read/write file locks).
  const input = await readFile(src);
  const base = sharp(input).resize({ width: MAX_W, withoutEnlargement: true });
  const meta = await base.metadata();
  const width = Math.min(meta.width ?? MAX_W, MAX_W);
  const height = meta.height && meta.width
    ? Math.round((meta.height * width) / meta.width)
    : meta.height ?? 0;

  const webpOut = join(dir, `${stem}.webp`);
  const avifOut = join(dir, `${stem}.avif`);

  const webpBuf = await base.clone().webp(WEBP).toBuffer();
  await writeFile(webpOut, webpBuf);
  const avifBuf = await base.clone().avif(AVIF).toBuffer();
  await writeFile(avifOut, avifBuf);

  return { file, width, height, jpg: input.length, webp: webpBuf.length, avif: avifBuf.length };
}

async function main() {
  const names = (await readdir(SHOTS_DIR))
    .filter((f) => /\.(jpe?g|png)$/i.test(f))
    .sort();

  const rows = [];
  let jpg = 0;
  let webp = 0;
  let avif = 0;

  for (const file of names) {
    const r = await processImage(SHOTS_DIR, file);
    rows.push(r);
    jpg += r.jpg;
    webp += r.webp;
    avif += r.avif;
    console.log(
      `${file.padEnd(38)} ${String(r.width) + 'x' + r.height} ` +
        `jpg ${kb(r.jpg).padStart(6)} -> webp ${kb(r.webp).padStart(6)} / avif ${kb(r.avif).padStart(6)}`
    );
  }

  // Logo -> WebP sibling (kept alongside the PNG fallback).
  const logoBuf = await readFile(LOGO);
  const logoWebp = await sharp(logoBuf).webp({ quality: 88, effort: 5 }).toBuffer();
  await writeFile(LOGO.replace(/\.png$/i, '.webp'), logoWebp);
  console.log(`\nlogo png ${kb(logoBuf.length)} -> webp ${kb(logoWebp.length)}`);

  // Dimensions manifest — keyed by the exact screenshot filename (shot.src).
  const entries = rows
    .map((r) => `  '${r.file}': { w: ${r.width}, h: ${r.height} },`)
    .join('\n');
  const ts =
    '// AUTO-GENERATED by scripts/guide/optimize-screenshots.mjs — do not edit by hand.\n' +
    '// Intrinsic dimensions of each guide screenshot, used to reserve exact space\n' +
    '// (aspect-ratio + width/height) so images never cause layout shift (CLS = 0).\n' +
    'export type ShotDim = { w: number; h: number };\n\n' +
    'export const SHOT_META: Record<string, ShotDim> = {\n' +
    entries +
    '\n};\n';
  await writeFile(MANIFEST, ts);

  console.log(
    `\nTOTAL  jpg ${kb(jpg)}  ->  webp ${kb(webp)} (${Math.round((1 - webp / jpg) * 100)}% smaller)  /  ` +
      `avif ${kb(avif)} (${Math.round((1 - avif / jpg) * 100)}% smaller)`
  );
  console.log(`Manifest: ${MANIFEST} (${rows.length} images)`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
