// Cover logo (LCP element). WebP with PNG fallback, fetchPriority high. The
// white ring/circle lives on the wrapper so it shows instantly (no blank flash,
// no layout shift) while the logo fades in. Same hang-proof timer+remount retry
// as GuideImg: a source that hangs on a cold start is abandoned and re-fetched.
function CoverLogo() {
  const [loaded, setLoaded] = useState(false);
  const [attempt, setAttempt] = useState(0);

  useEffect(() => {
    if (loaded || attempt > 12) return;
    const t = window.setTimeout(() => setAttempt((a) => a + 1), 3500);
    return () => window.clearTimeout(t);
  }, [loaded, attempt]);

  const bust = attempt === 0 ? '' : `?r=${attempt}`;

  return (
    <div className="h-28 w-28 rounded-full bg-white/95 p-2.5 shadow-2xl ring-2 ring-gold/70 md:h-36 md:w-36">
      <picture key={attempt}>
        <source type="image/webp" srcSet={`/guide/jerusalem-math-logo.webp${bust}`} />
        <img
          src={`/guide/jerusalem-math-logo.png${bust}`}
          alt="לוגו מחוז ירושלים"
          width={512}
          height={512}
          decoding="async"
          fetchPriority="high"
          onLoad={() => setLoaded(true)}
          onError={() => setAttempt((a) => a + 1)}
          className={cn(
            'h-full w-full rounded-full object-contain transition-opacity duration-300',
            loaded ? 'opacity-100' : 'opacity-0'
          )}
        />
      </picture>
    </div>
  );
}

// Real screenshot in a premium browser frame. When the shot maps to a live
